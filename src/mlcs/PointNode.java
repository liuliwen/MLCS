package mlcs;

public class PointNode {
	int point[];

	public PointNode(int[] point) {
		super();
		this.point = point;
	}

	public PointNode() {
		super();
	}

	public int[] getPoint() {
		return point;
	}

	public void setPoint(int[] point) {
		this.point = point;
	}
	

}
